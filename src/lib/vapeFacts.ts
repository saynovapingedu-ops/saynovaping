/** สุ่มลำดับแบบ deterministic จาก seed (เลี่ยงสลับซ้ำเดิม) */
export function shuffledFacts(seed: number): FactStatement[] {
  const arr = [...VAPE_FACTS];
  for (let i = arr.length - 1; i > 0; i--) {
    const j = (seed * 9301 + 49297 + i * 233) % (i + 1);
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}
